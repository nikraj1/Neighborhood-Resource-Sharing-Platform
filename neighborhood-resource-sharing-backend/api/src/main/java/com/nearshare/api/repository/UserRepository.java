package com.nearshare.api.repository;

import com.nearshare.api.entity.User;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

@Repository
public interface UserRepository extends JpaRepository<User, Long> {

    // Method to find user by email (needed for Login/Auth)
    User findByEmail(String email);

    // Note: findById(Long id) is already inherited from JpaRepository,
    // so it is automatically available for our wallet balance updates!
}









































































































































